        <h2>
            The Bethlehem Grand Bench Burner
        </h2>
        <p>
            Bethlehem Burners is proud to announce the release of our very first three-stage bench burner, The Grand! This stainless steel, surface mix torch combines our Alpha, Bravo and Champion torches into one easy to use Bench Burner! The Grand is the most versatile
            and fuel efficient glass working torch available today.
        </p>
        <p>
            The Grand is a stainless steel, surface mix bench burner with three separately controlled rings of fire. Each stage is controlled by two needle valves providing artists with unmatched control over flame size, heat potential and fuel mixture ratios. This
            torch produces soft enveloping flames that gently bathe glass with a clean even heat. The torch can easily produce a wide range of flames; from small and gentle for fine detail stringer work, to flames large enough to fully encompass 11”+
            diameter tubing*. The torch is mounted on Bethlehem’s scroll base positioning arm (<a href="https://www.youtube.com/embed/yutWhINmqTI" target="_blank">Click here to see the Scroll Base in action</a>), an upgrade from the traditional
            rack-n-pinion gear, enabling smooth torch adjustments that automatically lock in place. The perfect all around torch for glass artists working wide ranges of glass, e.g. glass size, color, and density.
        </p>
