        <p>
            The Cheetah is a 13 jet Triple Mix Surface Mix Torch. The Triple Mix technology produces the most efficient flame available for today's glassblower. By surrounding the fuel with oxygen and then injecting oxygen directly down the fuel stream, the fuel
            is completely burned for a hotter cleaner flame. The oxygen streams are independently controllable by the green and blue valves. This provides a wider flame range and chemistry that is easily adjustable by the operator without changing tips
            or torches.
        </p>
        <p>
            The Cheetah works soft and borosilicate glass with finesse and ease. The flame is hot and penetrating for borosilicate work up to 3" solid, and soft and clean to work the most delicate soft glass colors. The Cheetah flame will get as small as the diameter
            of a pencil up to a 3/4" wide 20" long flame. The flame is excellent for inside out work and production borosilicate pieces. The Cheetah is easily adjustable from a fuel rich reduction flame, to a clean neutral flame, to an oxygen rich oxidized
            flame, to an over-oxidized flame that is necessary for special glass effects.
