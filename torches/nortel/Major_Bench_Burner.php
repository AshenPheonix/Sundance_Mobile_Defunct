        <h5 class="font-weight-bold">
            Connecting your Hose to the Major Burner Torch
        </h5>
        <p>
            The hose slips on to the torch barbs and are secured with hose clamps.
            A 3/8" (ID) Type T twin gas hose is what you need.
            Our 3/8" twin gas hose will work great for this torch.
            Our #T128 twelve foot hose with one end cut - or
            Our #T258 twenty five hose (you need to cut the fittings off of one end.)
            A 1/4" (ID) hose will work but it take a bit of pushing to get the smaller hose in the barb.
        </p>
