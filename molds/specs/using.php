            Using ceramic slumping molds
        </p>
        <ol class="text-left">
            <li style='list-style-position:outside;margin-bottom:1em;'>
                <span class="font-weight-bold">Check sizes</span> - Be sure to double check the actual size of your mold before you cut the glass you are slumping, as all molds vary slightly in size.
            </li>
            <li style='list-style-position:outside;margin-bottom:1em;'>
                <span class="font-weight-bold">Prefiring</span> - Prefiring is recommended, especially for molds larger than 30 centimeters. Fire the kiln at 90&deg; Fahrenheit up to 1470&deg; Fahrenheit with no holding time. Cool down the mold in the kiln
            </li>
            <li style='list-style-position:outside;margin-bottom:1em;'>
                <span class="font-weight-bold">Kiln Wash</span> - Brush or spray the kiln wash on the mold and let it dry for approximately 
                20 minutes in the kiln at 400&deg; Fahrenheit. Make sure the vent holes in the mold 
                are open. Replace the kiln wash every 3rd or 4th firing, or when necessary. 
            </li>
            <li style='list-style-position:outside;margin-bottom:1em;'>
                <span class="font-weight-bold">Spacing</span> - When you slump your glass, place your mold on kiln posts. The distance between the 
                    mold and the heating elements should be no less than 2-3 centimeters. 
            </li>
            <li style='list-style-position:outside;margin-bottom:1em;'>
                <span class="font-weight-bold">Thermal Shocking</span> - Ceramic molds, like glass, are susceptible to thermal shock and should not be heated 
                or cooled too rapidly. Special attention is advised when using sidefiring kilns.
            </li>
        </ol>
