    <h2><br>
        <br> What are Murrini Canes?</h2>
    <br> There are a few different ways to spell Murrini: Murrini, murrhine. They also have been called
    <br>mosaic canes or rods. These are glass rods or canes with layered colored glass that creates
    <br>a design that only shows from the ends. When you buy a chip you get a section of the cane and
    <br>you can cut down this cane piece into several smaller chips that you can then melt into your
    <br>glass sculpture. This way you can add great details and images to your glass pieces! Make sure
    <br>that you use borosilicate Murrini for hard glass and Moretti Millifori for soft glass. <br>
    <br>
    <h2>What is Millifori?<br>
    </h2>
    Millifiori is a general term that literally means &quot;A thousand flowers&quot; in Italian. These are Murrini
    <br>canes that are composed of flower images instead of other images like stars, peace symbols,
    <br>or the Yin Yang (shown below). Most hard glass borosilicate canes are composed of these
    <br>other symbols and most soft glass Moretti canes have the Millifiori flower images.<br>
