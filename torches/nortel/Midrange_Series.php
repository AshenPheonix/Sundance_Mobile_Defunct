<h1>Nortel 1602 Midrange Series Torches</h1>
<br>
<h2>Torches for glass lampworkers, beadmakers, flameworkers, glassblowers, and marblemakers</h2>
<br>
<span class="bold block margin_10">Choose between three torch varieties.</span>
<ul class="alignLeft margin_left_20">
    <li class="disc">No top Tip/Flame - Bottom Tip/Flame only</li>
    <li class="disc">With a Pre-Mix top Tip/Flame</li>
    <li class="disc">With a Surface Mix top Tip/Flame - two choices!</li>
</ul>
<span class="bold block margin_10">Bottom Tip/Flame is the same on all three torch varieties.</span>
<p>The bottom torch tip is Adjustable!. With a single set of oxygen and gas valves, you can get a fairly small pointed flame or change to a larger bushy flame by adjusting the torch valves!</p>

<span class="bold block margin_10">REMEMBER</span>
<ul>
    <li>Surface Mix Tip works with all kinds of glass. Soft and Hard.</li>
    <li>Pre-Mix Mix Tips only work with Borosilicate Hard Glass (Pyrex) but is faster working.</li>
</ul>
