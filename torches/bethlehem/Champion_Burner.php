            <span class="font-weight-bold">The Champion Torch</span> The Champion Bench Burner is a two stage, stainless steel, surface mix flame working torch with separately controlled outer and inner flames. Each stage is controlled by one gas and
            one oxygen valve, and the torch comes standard with a four port manifold.
        </p>
        <p>
            The Champion was modeled after Bethlehemâ€™s famous PM2D Bench Burner and creates flames almost identical to the legendary burner! The low-velocity flames of the Champion torch allow flame workers to produce a wide range of fuel mixtures while keeping the
            flames workable and gentle to the glass. These flames bathe the glass in even heat and gently heat the glass from all sides, much like working glass in a furnace.
        </p>
        <p>
            This type of flame is excellent for every style of flame working, including large hollow work, solid milli work, color-mixing and fine detail work with difficult colors. The large diameter, low-velocity flames produced by the outer fire of the Champion,
            make evenly heating large amounts of glass with no cold spots a breeze! Even highly oxidizing flames are soft and even!
        </p>
