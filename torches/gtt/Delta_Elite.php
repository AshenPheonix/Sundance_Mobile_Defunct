        <p class="text-left">
            The Delta Elite torch is a 2 stage torch that uses the powerful Lynx 7 Jet for the center fire and 3 rings of jets with a total of 50 jets for the outer fire. This completely surface mix torch uses the patented Triple Mix technology and 57 total jets
            to provide unparalleled power for all types of glass.
        </p>
        <p class="text-left">
            The Delta Elite is great for soft or hard glass with the ability to produce many flame types from a soft bushy flame to a hard driving flame. The flame chemistry that is achieved with the Triple Mix technology has a wide range including a fuel rich reduction
            flame, a neutral flame, an oxygen rich oxidizing flame, and an over-oxidizing flame. These flame types make any type, color or effect of glass easy to work and manipulate.
        </p>
        <p class="text-left">
            The Delta Elite comes standard with a 4 stud manifold that allows for the most flexibility for any setup to be run. The primary use is for a foot pedal to be used to turn the outer fire on and off with the outer fire valves preset to the desired setting.
            Also the 4 stud can make use of different oxygen sources such as a concentrator on the center fire and tanked oxygen on the outer fire. In this situation the fuel will be "Y"ed together at the torch for simplicity. A foot pedal is NOT required
            for a 4 stud torch, the hoses can be "Y"ed back together to use a single fuel source and a single oxygen source with the option to use a foot pedal in the future.
        </p>
