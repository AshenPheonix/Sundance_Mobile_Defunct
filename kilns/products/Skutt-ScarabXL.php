        <img alt="Primary point rests" src="../images/skutt/PrimaryPR.jpg" class="float-left pad_8 img-fluid">
        <span class="red bold large">Primary Point Rests</span>
        <p class="alignLeft ">The Primary Point rests are designed to slide up and down to support your rods and tubes. If your handles are extremely long we would suggest adding the Optional Secondary Point Rest which extends further out from the kiln and is fully adjustable
            up and down.</p>

        <p class="alignLeft ">When you are ready to anneal you slide all 3 point rests up tight against the doggie doors to seal off the chamber.</p>

        <p class="alignLeft ">The point rests are constructed of solid brick and stainless steel for maximum life. To operate them simply loosen the the knob, slide the rest to the position desired and tighten the knob. It was specifically designed to be a one hand operation
            so you can hold your glass with the other hand.</p>
        <hr>
        <br>
        <img alt="Balanced main door" src="../images/skutt/MainDoorHinge.jpg" class="float-right pad_8 img-fluid">
        <span class="red bold large">Balanced Main door</span>
        <p class="alignLeft ">The Main Chamber Door hinge system has been completely redesigned with precision hardware for smoother operation. The door is balanced using an counterweight. The arms holding the counter weight were engineered with a precise bend that places
            the counterweight in a neutral position when the door is half open. This allows for enough weight when the door is down to keep it closed and keeps it from wanting to get away from you when you open it.</p>

        <p class="alignLeft ">The entire door is constructed of insulating fire brick surrounded by <span class="bold">heavy gauge stainless steel.</span> The handle is also constructed of stainless steel to prevent corrosion.</p>
        <br>
        <hr>
        <br>
        <br><img alt="Balanced main door" src="../images/skutt/DoggieDoors.jpg" class="float-left pad_8 img-fluid">
        <span class="red bold large">The "Doggie" Doors</span>
        <p class="alignLeft ">The Doggie Doors (sometimes referred to as bead doors) are the brainchild of master flameworker Marcel Braun. They are constructed using a channeled <span class="bold">stainless steel</span> frame which holds a section of soft fiberboard. When
            you close the door it rests gently on your glass handles, sealing in the heat without scratching the glass. If they ever wear out you can replace them easily and inexpensively. The doors are <span class="bold">counterbalanced</span> to stay
            open on their own with weighted wood <span class="bold">handles that stay cool to the touch.</span></p>

        <p class="alignLeft ">One of the most valued design elements of the Scarab and Scarab XL kilns is the introduction of the Center Door. The center door allows you to use a single larger diameter tube or rod without affecting the tight seal on the other rods. Without
            it the larger diameter tube would hold the doggie door up higher, allowing unnecessary heat to escape…Brilliant!</p>
        <br>
        <hr>
        <br>
        <img alt="Removable Chassis" src="../images/skutt/chassisopen.jpg" class="float-right pad_8 img-fluid">
        <span class="red bold large">Removable Chassis</span>
        <p class="alignLeft ">Let’s face it, these kilns are heavy!. The last thing you want to do is to start wrestling around with them to get at a component that needs to be replaced. All Scarab kilns are designed with a <span class="bold">Removable Chassis</span> which
            holds all the main electrical and electronic components. Just remove the screws that hold it in place and slide it out. You can even disconnect the power cord and ship the whole thing back to Skutt for testing if necessary.</p>

        <p class="alignLeft ">One of the most problematic components of kilns has traditionally been the relays. Mechanical relays have a limited life which is why Skutt has upgraded all Scarab Kilns with <span class="bold">Solid State Relays.</span> These relays or kept cool
            with a <span class="bold">fan mounted in the side</span> of the kiln (not necessary on the Mini Scarab because the Amps are so low).</p>
        <hr>
        <br>
        <span class="red bold large">Built in Forklift Channels</span>
        <p class="alignLeft ">Did we mention that these kilns are heavy? They weigh <span class="bold">770lbs!</span> Because of this each Scarab XL kiln is equipped with built in forklift channels for moving with a forklift or pallet jack.</p>
        <br>
        <hr>
        <br>
        <br>
        <img alt="Secondary point rest" src="../images/skutt/SPR.jpg" class="float-right pad_8 img-fluid">
        <span class="red bold large">Optional Secondary Point Rest</span>
        <p class="alignLeft ">The Optional Secondary Point Rests, <span class="bold">another unique design of Marcel Braun</span>, are generally ordered on just about every Scarab that goes out of here. Flameworkers love its ability to hold long handles, preventing their work
            from popping up off the floor. Its well thought out design allows you to move the rest to almost any position you want. It not only moves in and out but also moves up and down. The scalloped edge ensures that your rods will stay in place.
        </p> <span class="bold">Comes with kiln free of charge</span>
