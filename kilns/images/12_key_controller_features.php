    <p align="LEFT"> The Sentry 2.0 can be installed on all existing Paragon 12-key digital   kilns. It uses the same size faceplate as the earlier DTC 100 - 1000 series.   Upgrade a switch-operated kiln with the wall-mount version.  </p>
    <p align="LEFT"> Thermocouples  </p>
    <p align="LEFT"> The thermocouple senses temperature inside the kiln. The standard thermocouple   on most Paragon kilns equipped with the Sentry 2.0 is the ¼” Type-K. It   is hermetically sealed in a metal sheath for long life. It replaces the   earlier 1/8” thermocouple.  </p>
    <p align="LEFT"> Though a precision instrument, the thermocouple wears with use, resulting   in a gradual shift in temperature readout. (This wear occurs mostly above   2000°F.) If you fire pottery, consider the deluxe Type-S, noted for its   extreme longevity.  </p>
    <p align="LEFT"> Ramp-Hold Custom Programming  </p>
    <p align="LEFT"> Ramp-Hold allows you to infinitely adjust each part of the firing cycle.   Vary the firing to obtain different effects from the same glaze.  </p>
    <p align="LEFT"> Ramp-Hold programs are made of segments. Each segment has a temperature,   firing rate, and, if desired, a temperature hold.  </p>
    <ul>
      <li> <strong>Skip Segment</strong> skips past any segment.  </li>
      <li> <strong>Store Nine Programs:</strong> One 18-segment and eight 10-segment programs remain   in memory even when the kiln is unplugged.  </li>
      <li> <strong>Present Status</strong> shows which segment the kiln has reached during firing.  </li>
      <li> <strong>Cooling </strong>Control both heating and cooling. For deeper colors, especially   iron reds, experiment with slow cooling.  </li>
      <li> <strong>Hold</strong> is available in every segment.  </li>
