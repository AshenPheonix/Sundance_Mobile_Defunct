        <b>F16 Glass Fusing Kiln</b>
        <br>
        <br> F16&#39;s square digital glass kiln operates on a 120 volt outlet. Its square firing chamber makes it ideal for fusing glass tiles. <br>
        <br> You can also fire small glass bowls, pendants, earrings, cufflinks, and even bracelets. Try out the newest glass techniques taught in seminars and books. Enjoy making stunning dichroic glass jewelry. Make gifts that your friends will treasure.
        When you own a F16 , you can endlessly experiment with glass fusing.<br>
        <br> The F-16 heats from the top for even heat distribution across the glass surface. The lid elements are mounted in pinless element grooves. The elements do not need pins, because the element coils are wider than the groove opening. <br>
        <br> Optional Ceramic Fiber Top<br>
        <br> The standard F-16 comes with an insulating firebrick top. You can also upgrade your kiln with the optional deluxe ceramic fiber top, which also has pin less element grooves. The fiber top helps keep the kiln interior even cleaner. The fiber
        lid is a result of our search for better ways to make kilns.<br>
        <br>
        <b>Reliability at the Heart of the Kiln</b> The F-16 has a two-year warranty. The temperature sensor, called a thermocouple, is covered with high-nickel stainless steel sheath for long life. A heavy steel base supports the reversible brick bottom.
        These features are extra assurance that your F16 kiln is built to last. No extra stand is needed.<br>
        <br> The reliable F-16 has two mechanical relays for long life. (The relays turn on the heating elements after receiving a signal from the digital controller.) The two relays in the F-16 last longer than the single relay of competitive kilns,
        because less amperage goes through the dual relays of the F-16.<br>
        <br> In addition, you can order the mercury relay upgrade for your new kiln. The mercury relays are ordinarily found only on industrial kilns. The life of the quiet, reliable mercury relay has been measured in millions of on/off cycles.<br>
        <br>
        <b>Convenient Features</b> For your convenience, flip the switch on the front panel to turn off power to the controller when the kiln is not in use.<br>
        <br> An 18&quot; tall stand comes standard on the F-16. This stand raises the kiln to a comfortable height for loading and operation.<br>
        <br> The F-16 control box has a durable high temperature, water-base finish. We chose water-base paint because of the low emissions during spraying. This creates a safer working environment for our employees and cuts down on pollution.<br>
        <br>
        <b>Optional Glass Window and Bead Door</b> Beginners and even experts love the optional high temperature glass window. Watch every stage of fusing as your pieces begin to glow orange and then progress from tack fuse through full fuse. Watch the
        glass to fine-tune a digital program. (Always wear firing safety glasses while watching the glass through the window.)
        <br>
        <br> You can even anneal glass beads with your F-16. Order the optional bead door, which mounts to the front of the kiln. Use your kiln to safely cool even large, ornate beads through the annealing range.<br>
