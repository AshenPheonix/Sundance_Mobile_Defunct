<h3 class="white">Do you need a kiln for lampworking?</h3>
<strong><span class="style3">There are two reasons you need a kiln designed for 
  Lampworking:<br>
  <br>
  &nbsp;&nbsp;&nbsp; </span></strong>When you&#39;ve finished working a glass bead in your torch you need to cool it slowly enough to avoid thermal shock and cracking, which is what kilns are used for. However, it is important to note that<br> you may
still have stress in the bead even if you have cooled it without having it crack.
<br>
<br> &nbsp;&nbsp;&nbsp; A glass bead must also be properly annealed. Annealing is done by exposing your bead to a certain temperature for a particular period of time, which only a kiln can do. The bigger your bead,
<br> the more time it will take to anneal; the requisite temperature depends on the type of glass you<br> are using (Moretti is about 940&#176; F) and the length of time depends on the thickest part of
<br> the bead (a typical length might be 15 minutes, but longer doesn&#39;t hurt).
<br>
<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Beads can also be cooled then later put into a cold kiln and brought up to a given temperature for
<br> annealing. Alternatively, annealing can be done immediately by putting a bead, still on the<br> mandrel, into a hot kiln directly from a torch flame. Hot Glass kilns are specially designed so that beads can safely be put into them while they&#39;re
hot.
<br>
<br>
<hr>
<span class="style4"> <br>
  <strong>There are four popular methods of cooling glass beads.</strong></span><br>
<br>
<span class="style2"><strong>1. Cooling the bead by slowly removing it from the torch flame.<br>
  </strong><br>
  </span>This method is called flame annealing. Many artists use this method of annealing<br> even if they are using a kiln. This method may protect smaller sized beads from thermal
<br> shock and, if properly done can do some annealing for a bead of any size.
<br>
<br>
<br>
<span class="style2"><strong>2. Placing your beads between two layers of fiber blanket.<br>
  </strong> </span> <span class="style3">(<a href="lgtoolweb.php#anchor769704"><strong>See more about this</strong></a>)</span><br>
<br> This method may protect smaller beads from thermal shock,
<br> but does nothing to anneal your glass bead.
<br>
<br>
<br>
<span class="style2"><strong>3. Bury your bead deep in a hot crock pot filled with hot </strong> </span>
<a href="lgtoolweb.php#vermiculite"><span class="style2"><strong>vermiculite</strong></span></a><strong>. <br>
  </strong> <br> This method also may protect smaller beads from thermal shocking,<br> but also does nothing to anneal your glass bead. <br>
<br>
<br>
<span class="style2"><strong>4. Place your bead in a heated kiln.</strong></span> <br>
<br> Using a kiln will protect a bead of any size from thermal shock and will anneal your
<br> glass bead if kept at the correct temperature for a sufficient period of time.<br>
<br>
<br>
