        <h3>Features</h3>
        <strong>F-14 Glass Fusing Kiln</strong>
        <br>
        <br> Square F-14 digital glass kiln operates on a standard household outlet and fires up to nine 4&quot; x 4&quot; glass tiles. Imagine the murals you could make with this kiln.<br>
        <br> Fire small glass bowls, pendants, earrings, cufflinks, and even bracelets. Try out the newest glass techniques. Enjoy glittering dichroic glass. Make gifts that your loved ones will treasure. When you own a The F14 kiln, you can experiment
        with glass fusing to your heart&#39;s content.<br>
        <br> The F-14 heats from the top for even heat distribution across the glass surface. The lid elements are mounted in pin less element grooves. The groove does not need pins, because the element coils are wider than the groove opening. This helps
        eliminate dust in the firing chamber.<br>
        <br>
        <strong>Optional Ceramic Fiber Top</strong> The standard F-14 comes with an insulating firebrick top. You can also upgrade your kiln with the optional deluxe ceramic fiber top, which has the same type of pin less groove as the standard top. The
        fiber top helps keep the kiln interior even cleaner.<br>
        <br>
        <strong>Optional Glass Window and Bead Door</strong>
        <br>
        <br> Enhance your glass fusing experience with the optional high temperature 1&quot; x 3&quot; glass window. It simplifies fusing by enabling you to watch every stage, from softening, to tack fuse, to full fuse. Watch the glass to fine-tune a
        digital program. (Always wear firing safety glasses, available from Sundance , while watching the glass through the window.)<br>
        <br> You can even anneal glass beads with your F-14. Order the optional bead door, which mounts to the front of the kiln. Use your kiln to safely cool large, ornate beads through the annealing range.<br>
        <br>
        <strong>Reliability at the Heart of the Kiln</strong>
        <br>
        <br> The F-14 has a two-year warranty. The temperature sensor, called a thermocouple, is sheathed with high-nickel stainless steel for long life. A heavy steel base supports the reversible brick bottom completely. These features are extra assurance
        that your The F14 kiln is built to last. No extra stand is needed.<br>
        <br> The reliable F-14 has dual mechanical relays for long life. The digital controller sends a signal to the relays to turn on the heating elements. The two relays in the F-14 last longer than the single relay of competitive kilns because of
        the reduced amperage going through each relay.<br>
        <br> In addition, you can order the mercury relay upgrade with your new kiln. The mercury relays are ordinarily found only on industrial kilns and outlast mechanical relays. The life of the quiet, reliable mercury relay has been measured in millions
        of on/off cycles.<br>
        <br>
        <strong>Convenient Features</strong>
        <br>
        <br> For your convenience, flip the switch on the front panel to turn off power to the controller when the kiln is not in use.<br>
        <br> The F-14 control box has a durable high temperature, water-base finish. We chose water-base paint because of the low emissions during spraying. This creates a safer working environment for our employees and cuts down on pollution.<br>
