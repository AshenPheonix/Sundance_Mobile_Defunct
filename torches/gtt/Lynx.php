        <h3 class="font-weight-bold">
            GTT Lynx
        </h3>
        <p>
            The Lynx is a 7 jet, Triple Mix Surface Mix Torch. It was the first Triple Mix Torch developed by GTT. The Triple Mix technology produces the most efficient flame available for today's glassblower. By surrounding the fuel with oxygen and then injecting
            oxygen directly down the fuel stream, the fuel is completely burned for a hotter cleaner flame. The oxygen streams are independently controllable by the green and blue valves. This provides a wider flame range and chemistry that is easily
            adjustable by the operator without changing tips or torches.
        </p>
        <p>
            The Lynx has a small pinpoint flame for the utmost in detail work and finite heating. The Lynx works soft and borosilicate glass with finesse and ease. The flame is hot and penetrating for borosilicate work up to 2" solid, and soft and clean to work the
            most delicate soft glass colors. The Lynx is easily adjustable from a fuel rich reduction flame, to a clean neutral flame, to an oxygen rich oxidized flame, to an over-oxidized flame that is necessary for special glass effects.
