            Standard models require two valves wich are located on the back of the Wildcat burner.
        </p>
        <p class="text-left">
            Built into the end of the WildCat burner is the Vortex cooling feature. This feature uses pressurized oxygen from the oxygen manifold to inspirate atmospheric air through the collar at the end of the burner. The mixture of oxygen and air both support the flame and cool the end of the burner. The more oxygen used, the more effective this cooling feature becomes. If the WildCat is found to be overheating, inspect the cooling features porting to ensure that there are no blockages.
        </p>
        <p class="text-left">
            The WildCat has a very unique flame. Please note that based on the operator's prior burner experience, the flame settings may not look or sound as expected. The unique flame properties of the torch demonstrate superior performance. As such, until the operator becomes familiar with these unique properties, he/she should test out reactions with glass to determine whether the flame is reducing, neutral, or oxidizing.
        </p>
        <p class="text-left">
            For use with soft glass, Carlisle recommends a candle or focal length of 3/8". On a neutral setting, this will present a flame that is very hot and provides radiant heat around the flame. This radiant heat lessens the frequency of glass fractures due to temperature changes while moving in and out of the flame.
        </p>
        <p class="text-left">
            For use with hard glass, Carlisle recommends a candle or focal length of 3/4". Please note that a neutral setting of this flame may produce orange tips on the candles. However, as long as the candles are all the same length, this should not produce reduction on borosilicate colors.
        </p>
        <p class="text-left">
            Once a Neutral flame is achieved, adding more oxygen will produce the oxidizing flame. An oxidizing flame will make a more narrow flame pattern that can be used for more delicate work. It is important to note that the extra oxygen also has a cooling effect in the flame and will lower the heat potential.
        </p>
