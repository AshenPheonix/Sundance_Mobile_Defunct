        <p>
            The Scorpion produces flame sizes from a 2" pinpoint flame for detail work up to a flame of 1.25" wide and 16" long for wide beads and solid borosilicate work up to 3" in diameter. The Scorpion has excellent flame characteristics for soft glass and borosilicate
            glass that makes working easy and enjoyable. The colors of soft glass are easy to maintain and strike when necessary without burning or boiling. Borosilicate glass is easily worked by the penetrating heat of the Scorpion without waiting for
            the glass to get to working temperature. The Scorpion can completely melt through 3/4" solid borosilicate rod in 1 minute!
        </p>
        <p>
            The Scorpion is available in a 2 stud or 4 stud version to optimize any setup. The 2 stud is used when the fuel and oxygen come from a single source (or concentrators "Y"ed together), and when there is no desire to use a foot pedal now or in the future.
            The 4 stud allows for the most flexibility for any setup to be run. The primary use is for a foot pedal to be used to turn the outer fire on and off with the outer fire valves preset to the desired setting. Also the 4 stud can make use of
            different oxygen sources such as a concentrator on the center fire and tanked oxygen on the outer fire. In this situation the fuel will be "Y"ed together at the torch for simplicity.
        </p>
        <p>
            The 2 stud and 4 stud Scorpion both work identically in function, the only change is the way the fuel and oxygen are supplied to the torch. A foot pedal is NOT required for a 4 stud torch, the hoses can be "Y"ed back together to use a single fuel source
            and a single oxygen source with the option to use a foot pedal in the future.
        </p>
        <p>
            The Scorpion was designed with concentrators, low pressure and low consumption in mind. The Scorpion runs great on one 10 LPM concentrator or two 5 LPM concentrators with the optimal setup being two 8 LPM concentrators or tanked oxygen. Oxygen consumption
