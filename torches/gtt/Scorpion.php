            The Scorpion uses the mighty Cricket as the center fire for this 2 stage standard torch. The Cricket is well known for its efficiency on a 5 LPM concentrator and the great ability to work both soft glass and borosilicate glass with ease. The Scorpion
            has an 8 jet outer fire using our new patent pending 100% surface mix design that optimizes the available oxygen. The Scorpion makes the flame size of larger torches with fewer jets, owing the phenomenal performance to the newest GTT innovations
