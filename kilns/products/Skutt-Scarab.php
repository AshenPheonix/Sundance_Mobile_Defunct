                <span class="red bold">Carry Handles</span>
                <p class="alignLeft">Did we mention these kilns are heavy? Every Scarab kiln is equipped with two pairs of handles so the can be moved easier. The handles are ergonomically placed to balance the weight while you are carrying it and also positioned low enough
                    so you can lift it onto a table.</p>
