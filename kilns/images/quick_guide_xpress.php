  Press START/STOP at any time to stop a firing. To return to IdLE after the firing is completed, press START.
 Single-Speed Instructions
 Fires to a temperature, holds temperature if needed, then shuts
 off.
 <ol><li>From IdLE, press START.</li><br>
 <li>To select a firing speed, press the Up Arrow key repeatedly<br>
 until the desired firing speed appears.<br>
 SPd1 = 200°F or 111°C rate per hour<br>
 SPd2 = 500°F or 277°C rate per hour<br>
 SPd3 = 1000°F or 555°C rate per hour<br>
 SPd4 = 1500°F or 833°C rate per hour<br>
 SPd5 = Full Power<br>
 Then press START.</li><br>
 <li>Use the arrow keys to enter the temperature you are firing to.<br>
 Press START.</li><br>
 <li>Use arrow keys to enter the hold time (hours.minutes). (If not<br>
 needed, enter 00.00.) Press START. Strt will appear.</li><br>
 <li>Press START again. -On- will appear. The kiln is firing.</li></ol><br>
 <span class="stylebold"><span class="styleredcf">Ramp-Hold Instructions</span><br>
 Fires up to 8 speeds and temperatures (segments). Controls<br>
 heating and cooling.</span><br>
 <ol><li>From IdLE, press START.</li><br>
 <li>Press the Up Arrow key. Select PrO1, PrO2, PrO3, or PrO4.<br>
 Press START.</li><br>
 <li>rA 1 will appear. Use the arrow keys to enter firing rate<br>
 (temperature change per hour) for segment 1. (1° = slowest<br>
 rate. 1799°F/999°C = full power.) Press START.</li><br>
 <li>°F 1 or °C 1 will appear. Use the arrow keys to enter the<br>
 temperature you are firing to. Press START.</li><br>
 <li>HLd 1 will appear. Use the arrow keys to enter the hold time<br>
 (hours.minutes). (If not needed, enter 00.00.) Press START.</li><br>
 <li>Continue entering values for the segments needed. When rA<br>
 appears for the next segment that you don’t need, select 0000.<br>
 (This will zero out any remaining unused segments.) Then<br>
 press START. Strt will appear.</li><br>
 <li>Press START again. -On- will appear. The kiln is firing.</li><br>
 </ol>
