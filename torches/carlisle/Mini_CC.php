            The Carlisle Mini CC is a totally surface mixed burner that offers a high level of versatility in the flame. This wide range of flame capabilities makes the Mini CC the perfect bench burner for beginners and experts alike.
        </p>
        <p class="text-left">
            There are two versions of the Mini CC. The High Oxygen Bench Burner is suited for use with both hard and soft glass. It has been designed for increased productivity, attainable through higher flame temperatures and increased flow capabilities of gases. The second option, is the Pinpoint Bench Burner which produces a sharper, narrow flame with a longer focal length and superior piercing capabilities.
        </p>
        <p class="text-left">
            The body of the mini CC is constructed of brass and has cooling fins cut into its profile to help protect the burner from heat build-up. The tube matrix, which is constructed of seven tubes, is made of stainless steel. The stand itself is designed for ease of use and places the burner head at a 45° angle. The connection of the body to the stand is made with a ball joint, allowing for an increased or decreased tilt of 15° and articulation. This allows the burner to have excellent positioning capabilities. The base also has four rubber feet to reduce the likelihood of the unit from sliding during use and features two mounting holes for permanent installations.
        </p>
        <p class="text-left">
            Consumption based on standard usage is approximately 1.5 LPM (Liters Per Minute) of propane and 7 LPM of oxygen. This burner may be used with oxygen and either natural gas, propane, or even hydrogen. The recommended operating pressures are 2 PSI (Pounds per Square Inch) for fuel gas and 5 PSI for oxygen.
