        <p class="font-weight-bold">Carlisle CC glass torches are pre mix center fire with a surface mix outer burner</p>
        <h4 class="font-weight-bold">Carlisle CC</h4>
        <p class="text-left">
            The Carlisle CC is known as a cannon style burner. Their large outer case and tremendous flame output resembles the profile of a "cannon". The CC burner is capable of producing 276,100 BTU/hr at 5 P.S.I. natural gas and 10 P.S.I. oxygen.
        </p>
        <p class="text-left">
            The Carlisle CC produce two distinctive flames, the center fire (pre-mixed) and the outer fire (surface mixed). The center fire, which has characteristics of a single ported tip, produces a pre-mixed flame similar to what may be achieved with a Carlisle
            hand torch. This center fire tip is standard with piloting. Depending on the fuel gases used, the center fire is available without piloting. Natural gas and propane use a piloted center fire tip, while an unpiloted tip must be used with hydrogen.
            The surface mixed outer fire consists of a set of stainless steel tubes that make up a matrix around the center fire. There are two concentric rings of tubes developing the outer fire of the CC burner.
        </p>
        <p class="text-left">
            The outer shell of the CC series body is manufactured from brass (SS optional). The brass shell is threaded onto a bronze machined casting, which represents the mixer body. For fuel gas supply, four metering valves are attached to the back of the mixer
            body. These valves are color coded for ease of use. The oxygen valves have a green handle while the gas valves have red handles. These valves are standard interconnected by crossover attachments to limit the feed hose requirements to two.
            The CC series is also available with four separate inlets to allow for the use of foot controllers. The standard connection type for this configuration is hose adapters. Other connection types are available such as welding hose and JIC connections.
            These connection types are explained further in the options and accessories section.
        </p>
        <p class="text-left">
            The CC series can accept and interchange several different styles of base attachments. The standard style is a Ball & Post mount. The Ball & Post utilizes the horseshoe shaped cast iron base with a ball and socket connection. The post that extends upward
            is threaded directly into the bottom of the CC mixer body. This style offers a selective articulation. In order to adjust the angle of the head, the user must manually release a thumb screw in the cast iron base. This provides positive burner
            positioning. The Rack & Pinion style mount utilizes a pinion gear to pivot the burner in an arc. This style offers greater range in positioning than the Ball & Post mount. The user can adjust a knob on the side of the mounting device for quick,
            accurate positioning. Custom lathe mounts are also available as an option.
        </p>
        <p class="text-left">
            The Heat Exchanger is standard with any CC series burner. Manufactured from aluminum, the Heat Exchanger dissipates heat from the burner body. The Heat Exchanger has cooling fins providing increased surface area for the transfer of heat. This keeps the
            burner cool during operation. Some applications with high radiant heat transfer, will find our water cooling option desirable.
        </p>
