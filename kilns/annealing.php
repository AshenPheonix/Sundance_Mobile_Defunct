<br>
<span class="style2"><strong>How is a Lampworking Kiln different from a Ceramic Kiln?</strong></span><br>
<br> A ceramic kiln could work for bead making, but a lampworking kiln is safer for such
<br> projects for a couple of reasons. Keep in mind that beads are put into kilns while they
<br> are on and at very high temperatures.<br>
<strong><br>
  Electrocution - </strong>If you put a bead, still on the mandrel, into a ceramic kiln and you accidentally<br> touch one of the exposed element wires, you will get electrocuted! Our lampworking kilns will
<br> protect you from electrocution.
<br>
<strong><br>
  Heat</strong> - Yes! When a kiln is used for glass bead making, it is kept at about 1000&#176; F.
<br> If you open a ceramic kiln at 1000&#176;F to put in a bead you risk exposure to very intense
<br> heat. Our Hot Glass kilns have special openings on the sides that allow you to insert your
<br> beads safely. Simply lift up the loose cloth door and place the bead on the rack.
